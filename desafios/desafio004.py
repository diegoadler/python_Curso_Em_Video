a = input('Digite alguma coisa: ')

print('o tipo primitivo desse desse valor é ', type(a))
print('as palavras contem espaços', a.isspace())
print('o texto é numerico', a.isnumeric())
print('o texto é, maiusculo', a.isupper())
print('tem letras minusculas', a.islower())
print('é alfanumerico', a.isalpha())
print('é um numero', a.isalnum())
print('decimal', a.isdecimal())



